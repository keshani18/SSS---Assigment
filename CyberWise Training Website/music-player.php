    <h1 style="text-align: center; color:orange;">What is Phishing?</h1> <br> 
            <p style="font-size: 18px; color:white;">&#9898;  Phishing attacks are the practice of sending fraudulent communications that appear to come from a reputable source. It is usually done through email. The goal is to steal sensitive data like credit card  <br> and login information,or to install malware on the victim's machine.</p>
    <br>
    
    <br>
    <h3 style="text-align: left; color:orange;">   What are the Different Types of Phishing Attacks?</h1> <br>
    <p style="font-size: 18px; color:white;">01. Spear Phishing <br> 02. phishing Whaling <br> 03. Clone phishing<br>
        04. Smishing<br> 05. Vishing<br> 06. Link manipulation<br>07. Website forgery<br>08. Pharming. </p>
    <br>
    <br>
    <h3 style="text-align: left; color:orange;">01. What is Spear Phishing?</h1> <br>

<p style="font-size: 18px; color:white;">&#9898; Spear phishing is an email or electronic communication targeted toward a specific individual or organization. Although spear phishing is often used to obtain sensitive data, cybercriminals may also use it to install malware on a target's computer.</p><br>
    <br>

      <h3 style="text-align: left; color:orange;">02. What is Whaling?</h1> <br>

<p style="font-size: 18px; color:white;"> &#9898;Whaling is a form of spear phishing that targets senior executives or high-profile targets. Whale hunter's phishing messages are targeted at the individual and their role in an organization.</p><br><br>
    

        <h3 style="text-align: left; color:orange;">03. What is Clone Phishing?</h1> <br>

<p style="font-size: 18px; color:white;"> &#9898;Clone phishing is a form of phishing where a legitimate and previously delivered email is used to create an almost identical phishing email.
In the phishing email, the attachments or links in the email are replaced with a malicious version. It is then sent from a spoofed email designed to appear to come from the original sender. It may even claim to be a follow up or updated version of the original email/attachment. </p><br><br>

          <h3 style="text-align: left; color:orange;">04. What is Smishing?</h1> <br>

<p style="font-size: 18px; color:white;">  &#9898;Smishing or SMS phishing is phishing by SMS. Just like email phishing, smishing messages typically include a threat or enticement to click a link or call a number to create a sense of urgency and improve the probability of success.   </p><br><br>

            <h3 style="text-align: left; color:orange;">05. What is Vishing?</h1> <br>

<p style="font-size: 18px; color:white;">  &#9898;Vishing or voice phishing is conducted by phone and often targets users of Voice over IP (VoIP) services like Skype. 

Improvements in technology have made it easier for scammers to spoof caller IDs, so it can appear from a local area code or even a trusted organization.   </p><br><br>

              <h3 style="text-align: left; color:orange;">06. What is Website Forgery?</h1> <br>

 <p style="font-size: 18px; color:white;">  &#9898;Website forgery uses JavaScript to alter the address bar, making it hard to know whether you are on a phishing site. Attackers may place an image of the legitimate URL over the address bar or close the original bar and open up a new one with the legitimate URL without redirecting away from the fake site.   </p> <br><br>

                <h3 style="text-align: left; color:orange;">07. What is Pharming?</h1> <br>

<p style="font-size: 18px; color:white;">   &#9898;Pharming is a phishing attack that relies on a form of man-in-the-middle attack called DNS cache poisoning that redirects users from a legitimate site to a phishing site even if they type in the correct domain name.  </p>


